var mammothEl = $('#mammoth');

//on click, reveal the box with Information
mammothEl.click(function(){
  console.log('Mammoth Clicked')
  //Find info box in details els
  var infoBoxEl = $('#mammothDetails');
  infoBoxEl.toggleClass('hidden');
});

